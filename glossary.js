/* Регулярки для автоподсветки терминов в теории:
   основа слова (без окончания) + любые русские буквы после. */
const GLOSS_RE = {};
(function () {
  const vow = 'еёяьыаоуий';
  Object.keys(GLOSS).forEach(key => {
    if (GLOSS[key].abbr) { GLOSS_RE[key] = new RegExp('(' + key + ')', 'i'); return; }
    const stems = key.split(' ').map(w => {
      let s = w;
      while (s.length > 4 && vow.includes(s[s.length - 1])) s = s.slice(0, -1);
      return s + '[а-яё]*';
    });
    GLOSS_RE[key] = new RegExp('(' + stems.join('\\s+') + ')', 'i');
  });
})();
